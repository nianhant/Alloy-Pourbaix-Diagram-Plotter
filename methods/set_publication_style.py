import matplotlib.pyplot as plt

def set_publication_style():
    plt.rcParams.update({
        'font.size': 32,
        'axes.labelsize': 32,
        'axes.titlesize': 27,
        'xtick.labelsize': 27,
        'ytick.labelsize': 27,
        'legend.fontsize': 14,
        'figure.titlesize': 27,
        'savefig.dpi': 300,
    })

